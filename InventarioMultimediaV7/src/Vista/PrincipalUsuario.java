    private void asignaImg(String cad1,String tabla, String cad3, int n, JButton b){
    try {
            PreparedStatement ps = null;
            ImageIcon foto;
            InputStream is;
            ResultSet rs = null;
            UConnection conn = new UConnection();
            Connection con = UConnection.getConnection();
            String sql = "SELECT   "+cad1+" FROM "+tabla+"  WHERE "+cad3+"=?";
            ps = con.prepareStatement(sql);
            ps.setInt(1, n);
            rs = ps.executeQuery();
            ResultSetMetaData rsmd = rs.getMetaData();
            int columnas = rsmd.getColumnCount();
            while (rs.next()) {
            is = rs.getBinaryStream(1);
            BufferedImage bi = ImageIO.read(is);
            foto = new ImageIcon(bi);
            Image img = foto.getImage();
            Image newImg = img.getScaledInstance(140, 170, java.awt.Image.SCALE_SMOOTH);
            ImageIcon newIcon = new ImageIcon(newImg);
            b.setIcon(newIcon);
            }
        } catch (SQLException e) {
            System.out.println(e.toString());
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(rootPane, "exception" + ex);
        }    
        
        
        
    }

